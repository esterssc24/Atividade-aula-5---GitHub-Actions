def metros_para_km(metros):
    """Converte metros para quilômetros."""
    return metros / 1000

def metros_para_milhas(metros):
    """Converte metros para milhas."""
    return metros * 0.000621371

def km_para_metros(km):
    """Converte quilômetros para metros."""
    return km * 1000

def milhas_para_metros(milhas):
    """Converte milhas para metros."""
    return milhas / 0.000621371
